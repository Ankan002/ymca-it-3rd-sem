q=8
for i in range (q):
    print(i,end='  ')
print('')
for i in range (1,q):
    print(i)
print('')
for i in range (2,q,1):
    print(i)
print('')
for i in range (2,q,2):
    print(i)
print('')
for i in range (10,q,2):
    print(i)
print('')
